# include <iostream>

int main(){
	int edad = 46;
	std::cout  << "Mi edad es de: " << edad << " años"  << std::endl;
	edad += 5;
	std::cout  << "Mi edad es de: " << edad << " años"  << std::endl;
	edad -= 5;
	std::cout  << "Mi edad es de: " << edad << " años"  << std::endl;
	edad *= 5;
	std::cout  << "Mi edad es de: " << edad << " años"  << std::endl;
	edad /= 5;
	std::cout  << "Mi edad es de: " << edad << " años"  << std::endl;
	return 0;
}
